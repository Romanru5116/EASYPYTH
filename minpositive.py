#На вход программе поступает список из целых чисел. Ваша задача — найти в данном списке наименьшее положительное значение. В случае, если положительных значений нет, выведите строку «Empty».
# Читаем числа из ввода
numbers = list(map(int, input().split()))

# Отбираем только положительные числа
positive_numbers = [n for n in numbers if n > 0]

# Проверяем, есть ли в списке хоть одно положительное число
if not positive_numbers:
    result = "Empty"
else:
    # Находим минимальное из положительных
    result = min(positive_numbers)

print(result)
