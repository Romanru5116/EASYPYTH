#Программа на Python, которая использует цикл while для перебора чисел от a до b, пропуская те, что делятся на 2 или 3, и завершая работу при встрече с числом, кратным 777, не выводя его.

# Запрашиваем у пользователя начальное и конечное числа
a = int(input())
b = int(input())

# Инициализируем счетчик
current_number = a

# Цикл while для перебора чисел
while current_number <= b:
    # Условие 1: Проверить, кратно ли число 777
    if current_number % 777 == 0:
        
        break  # Принудительно завершаем цикл

    # Условие 2: Пропустить числа, кратные 2 или 3
    if current_number % 2 == 0 or current_number % 3 == 0:
        current_number += 1  # Переходим к следующему числу
        continue  # Пропускаем остаток цикла и переходим к следующей итерации

    # Если число не кратно 2 или 3 (и не 777), просто его выводим
    print(current_number)

    # Переходим к следующему числу
    current_number += 1
