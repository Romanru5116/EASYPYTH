#Возьмём число. Умножим его на его же первую цифру. Результат умножим на первую цифру результата. И так далее. Например, начнём с 8:



a = input()
c = 0
d = ''

# Чтобы d[0] не выдавал ошибку, сначала проверяем, что d не пустая
while not d or (d[0] != '1' and c < 1000000000):
    b = a[0]     # Берем первую цифру из текущей строки a
    a_int = int(a) 
    b_int = int(b)
    
    c = a_int * b_int
    d = str(c)
    a = d        # Обновляем a, чтобы цикл мог продолжаться с новым значением
    
    # Ограничитель для безопасности (чтобы не зависнуть навсегда)
    if len(d) > 15: 
        break
print(c) 
